from turtle import *
def poligono(a,b):
    for i in range(b):
        forward(a)
        right(360/b)
def pol2(a,b):
    for i in range(b):
        forward(a)
        left(360/b)
def pol3(a,b,d):
    c = int(b / d)
    for i in range(c):
        forward(a)
        left(360/b)
def pol4(a,b,d):
    c = int(b / d)
    for i in range(c):
        forward(a)
        right(360/b)
